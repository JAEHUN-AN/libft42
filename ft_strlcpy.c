#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	size;

	if (!dst || !src)
		return (0);
	size = ft_strlen(src);
	if (size < dstsize)
		ft_memcpy(dst, src, size + 1);
	else if (dstsize)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (size);
}
